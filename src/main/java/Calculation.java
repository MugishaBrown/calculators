public class Calculation {

    public int add(int numberA, int numberB) {
        int sum = numberA + numberB;
        return sum;
    }

    public int substract(int numberC, int numberD) {
        int difference = numberD - numberC;
        return difference;
    }

    public int multiply(int numberE, int numberF) {
        int multiplication = numberE * numberF;
        return multiplication;
    }

    public float division(int numberC, int numberD) {
        float division = numberC / numberD;
        return division;
    }
}

